// Afficher les chansons
function displaySongs() {
  // Calculer l'index de début et de fin
  const startIndex = (currentPage - 1) * songsPerPage;
  const endIndex = startIndex + songsPerPage;
  const paginatedSongs = filteredSongs.slice(startIndex, endIndex);

  // Vider le conteneur
  playlistContainer.innerHTML = "";

  // Ajouter chaque chanson
  paginatedSongs.forEach((song) => {
    const songCard = createSongCard(song);
    playlistContainer.appendChild(songCard);
  });
}

// Créer une carte de chanson
function createSongCard(song) {
  const card = document.createElement("div");
  card.className = "song-card";

  // Convertir la durée en minutes:secondes
  const minutes = Math.floor(song.duree / 60);
  const seconds = song.duree % 60;
  const durationFormatted = `${minutes}:${seconds < 10 ? "0" : ""}${seconds}`;

  // Générer une couleur aléatoire pour l'image de fond
  const colors = [
    "#e74c3c",
    "#3498db",
    "#2ecc71",
    "#f39c12",
    "#9b59b6",
    "#1abc9c",
  ];
  const randomColor = colors[Math.floor(Math.random() * colors.length)];

  card.innerHTML = `
        <div class="song-image" style="background-color: ${randomColor};">
            <div class="song-basic-info">
                <h3>${song.titre}</h3>
                <span class="song-artist">${song.artiste}</span>
            </div>
        </div>
        <div class="song-details">
            <div class="song-meta">
                <span class="song-genre">${song.genre}</span>
                <span class="song-duration">
                    <i class="fas fa-clock"></i> ${durationFormatted}
                </span>
            </div>
        </div>
    `;

  return card;
}
// Configuration de la pagination
function setupPagination() {
  const totalPages = Math.ceil(filteredSongs.length / songsPerPage);

  // Désactiver/activer les boutons précédent/suivant
  prevPageBtn.disabled = currentPage === 1;
  nextPageBtn.disabled = currentPage === totalPages || totalPages === 0;

  // Mettre à jour les numéros de page
  pageNumbers.innerHTML = "";

  for (let i = 1; i <= totalPages; i++) {
    const pageNumber = document.createElement("span");
    pageNumber.className = `page-number ${i === currentPage ? "active" : ""}`;
    pageNumber.textContent = i;
    pageNumber.addEventListener("click", () => goToPage(i));
    pageNumbers.appendChild(pageNumber);
  }
}

// Aller à une page spécifique
function goToPage(page) {
  currentPage = page;
  displaySongs();
  setupPagination();
  window.scrollTo({ top: 0, behavior: "smooth" });
}

// Page précédente
function goToPrevPage() {
  if (currentPage > 1) {
    goToPage(currentPage - 1);
  }
}

// Page suivante
function goToNextPage() {
  const totalPages = Math.ceil(filteredSongs.length / songsPerPage);
  if (currentPage < totalPages) {
    goToPage(currentPage + 1);
  }
}

